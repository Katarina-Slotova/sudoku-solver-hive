#include <stdio.h>

void    ft_print(int** grid)
{
    int i;
    int j;

    i = 0;
    while(i < 9)
    {
        j = 0;
        while (j < 9)
        {
            putchar(grid[i][j]+ '0');
            if (j < 8)
                putchar(' ');
            j++;
        }
        putchar('\n');
        i++;
    }
}
